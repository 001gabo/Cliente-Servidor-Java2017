    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            con=conexion_mysql.getConnection();
            
            String condicion=pivote.getText();

            pst=con.prepareStatement("update libros set lib_name=?, lib_descrip=?, lib_cantidad=?, lib_edicion=?, lib_year=?,lib_codigo=?, lib_observacion=? where id_lib ='"+condicion+"'");
            pst.setString(1,in_name.getText());
            pst.setString(2,in_desc.getText());
            pst.setInt(3,Integer.parseInt(in_cantidad.getText()));
            pst.setString(4,in_edicion.getText());
            pst.setInt(5,Integer.parseInt(in_anio.getText()));
            pst.setString(6,in_cod.getText());
            pst.setString(7,in_observacion.getText());
            pst.executeUpdate();
            con.close(); 
            open =new crud_libros();
            open.setVisible(true);
            this.setVisible(false);

        }
        catch(Exception ex){

            JOptionPane.showMessageDialog(null,("error"+ex));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

